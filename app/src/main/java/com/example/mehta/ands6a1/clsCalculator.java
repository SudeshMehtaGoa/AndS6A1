/* Simple calculator class to demonstrate JAVA test features */
public class clsCalculator {
    public int addTwoNumbers(int num1, int num2)
    {
        return (num1+num2);
    }
    public int subtractTwoNumbers(int num1, int num2)
    {
        return (num1-num2);
    }
    public int multiplyTwoNumbers(int num1, int num2)
    {
        return (num1*num2);
    }
    public int divisionTwoNumbers(int num1, int num2)
    {
        return (num1/num2);
    }
}
